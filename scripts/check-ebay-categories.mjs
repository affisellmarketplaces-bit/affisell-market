function normalize(s) {
  return s
    .normalize("NFD")
    .replace(/\p{M}/gu, "")
    .toLowerCase()
    .replace(/[’']/g, "'")
    .replace(/\s+/g, " ")
    .trim()
}

const prisma = new PrismaClient()

const rows = await prisma.category.findMany({
  select: { id: true, name: true, fullPath: true, isLeaf: true },
})

const byExact = new Map()
const byNorm = new Map()
for (const r of rows) {
  byExact.set(r.name, r)
  const n = normalize(r.name)
  if (!byNorm.has(n)) byNorm.set(n, [])
  byNorm.get(n).push(r)
}

const exact = []
const fuzzy = []
const missing = []

for (const label of EBAY_LABELS) {
  if (byExact.has(label)) {
    exact.push(label)
    continue
  }
  const n = normalize(label)
  const normHits = byNorm.get(n) ?? []
  if (normHits.length > 0) {
    fuzzy.push({ label, match: normHits[0].fullPath })
    continue
  }
  // partial: name contained in fullPath or vice versa
  const partial = rows.filter((r) => {
    const rn = normalize(r.name)
    return rn.includes(n) || n.includes(rn)
  })
  if (partial.length === 1) {
    fuzzy.push({ label, match: partial[0].fullPath, partial: true })
  } else if (partial.length > 1) {
    fuzzy.push({ label, match: `${partial.length} candidats`, partial: true })
  } else {
    missing.push(label)
  }
}

console.log(JSON.stringify({ total: EBAY_LABELS.length, exact: exact.length, fuzzy: fuzzy.length, missing: missing.length }, null, 2))
console.log("\n--- EXACT ---")
for (const x of exact) console.log("✓", x)
console.log("\n--- FUZZY / PARTIAL ---")
for (const x of fuzzy.slice(0, 40)) console.log("≈", x.label, "→", x.match)
if (fuzzy.length > 40) console.log(`… +${fuzzy.length - 40} autres`)
console.log("\n--- ABSENT (eBay label, pas de correspondance Affisell) ---")
for (const x of missing) console.log("✗", x)

await prisma.$disconnect()
